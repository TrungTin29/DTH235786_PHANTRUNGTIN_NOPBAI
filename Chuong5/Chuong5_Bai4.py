'''Dưới đây là một số hàm quan trọng trong xử lý chuỗi của Python:

len(s): Trả về độ dài chuỗi s.
s.upper(): Chuyển tất cả ký tự trong chuỗi thành chữ hoa.
s.lower(): Chuyển tất cả ký tự trong chuỗi thành chữ thường.
s.strip(): Loại bỏ khoảng trắng ở đầu và cuối chuỗi.
s.replace(old, new): Thay thế tất cả chuỗi con old bằng new.
s.split(sep): Tách chuỗi thành danh sách các chuỗi con dựa trên ký tự phân tách sep.
s.find(sub): Trả về vị trí đầu tiên của chuỗi con sub trong chuỗi, nếu không tìm thấy trả về -1.
s.count(sub): Đếm số lần xuất hiện của chuỗi con sub trong chuỗi.
s.startswith(prefix): Kiểm tra chuỗi có bắt đầu bằng prefix không.
s.endswith(suffix): Kiểm tra chuỗi có kết thúc bằng suffix không.'''